int	ft_atoi(char *str)
{
	unsigned int	i;
	unsigned int	nbr;
	unsigned int	count;

	i = 0;
	nbr = 0;
	count = 0;
	while (str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			count++;
		i++;
	}	
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	if (count % 2 != 0)
		return (-nbr);
	else
		return (nbr);
}

